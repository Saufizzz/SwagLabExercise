#Tutorial to create jenkins environment localhost

#1. download java installer "https://www.oracle.com/java/technologies/downloads/#java11"
#2. Install Java
#3. Add Java to System Path Advanced system settings "C:\Program Files\Java\jdk-11\bin"
#4. Open cmd prompt and check java -version
#5. navigate to jenkins.war file path directory using cd command "cd %USERPROFILE%\Downloads"
#6. run "java -jar jenkins.war"
#7. copy authentication key - password
#8. open localhost port 8080 usually jenkins run on this port
#9. Keyin authentication key - password
#10 Install plugins
